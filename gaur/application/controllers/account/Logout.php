/**
 * Account logout
 *
 * @package     Gaur
 * @subpackage  Controller
 * @author      Krishnan <krishnan57474@gmail.com>
 */
class Logout extends CI_Controller
{
    /**
     * Default page for this controller
     *
     * @return  void
     */
    public function _remap()
    {
        $this->load->helper('url');

        if (isset($_SESSION['user_id']))
        {
            session_destroy();
        }

        redirect('', 'location');
    }
}